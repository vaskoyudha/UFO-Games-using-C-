void display_status(std::string answer, std::vector <char> incorrect){
        std::cout << "Incorrect Guesses";
        for (int i = 0; incorrect.size() > i; i++){
            std::cout << incorrect[i] << " ";
        }
        std::cout << "Codeword: \n";
        for (int i = 0; answer.length() > i; i++){
            std::cout << answer[i] << " ";
        }
}
